package uff.ic.devweb.sistemabancario.model;

/**
 *
 * @author mila
 */
public class Usuario {

    public long getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public String getEmail() {
        return email;
    }

    public String getSenha() {
        return senha;
    }

    public long id;
    public String nome;
    public String email;
    public String senha;

    public Usuario(long id, String nome, String email, String senha) {
        this.id = id;
        this.nome = nome;
        this.email = email;
        this.senha = senha;
    }
}
